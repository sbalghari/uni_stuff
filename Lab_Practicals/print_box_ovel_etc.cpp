#include <iostream>
using namespace std;

int main() {

    // Box
    cout << "*****" << endl;
    cout << "*   *" << endl;
    cout << "*   *" << endl;
    cout << "*   *" << endl;
    cout << "*****" << endl;

    cout << endl;

    // Oval
    cout << "  ***  " << endl;
    cout << " *   * " << endl;
    cout << "*     *" << endl;
    cout << " *   * " << endl;
    cout << "  ***  " << endl;

    cout << endl;

    // Arrow
    cout << "   *   " << endl;
    cout << "  ***  " << endl;
    cout << " ***** " << endl;
    cout << "   *   " << endl;
    cout << "   *   " << endl;
    cout << "   *   " << endl;

    cout << endl;

    // Diamond
    cout << "   *   " << endl;
    cout << "  * *  " << endl;
    cout << " *   * " << endl;
    cout << "  * *  " << endl;
    cout << "   *   " << endl;

    return 0;
}